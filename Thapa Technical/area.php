<?php
# area of circle
$pi = 3.1416;
$rad = 5;
$areaOfCircle = $pi * $rad * $rad;
echo $areaOfCircle;

# area of triangle
$height = 5;
$base = 10;
$areaOfTriangle = .5*$height*$base;
echo $areaOfTriangle;

